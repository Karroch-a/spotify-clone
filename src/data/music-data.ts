export function getAllSongs(): Song[] {
  const allSongs = [
    ...trendingSongs,
    ...popularAlbums.map((album) => album.featuredSong),
    ...popularArtists.map((artist) => artist.topSong),
    ...popularRadio.map((radio) => radio.featuredSong),
    ...featuredCharts.map((chart) => chart.featuredSong),
    ...getLoudPlaylists.map((playlist) => playlist.featuredSong),
  ];

  return Array.from(new Map(allSongs.map((song) => [song.id, song])).values());
}

export function getSongById(id: string): Song | undefined {
  return getAllSongs().find((song) => song.id === id);
}

export function getAllPlaylists(): Playlist[] {
  const playlists: Playlist[] = [
    ...featuredCharts.map((chart) => ({
      id: chart.id,
      name: chart.title,
      description: chart.description,
      image: chart.image,
      songs: [chart.featuredSong],
    })),
    ...getLoudPlaylists.map((playlist) => ({
      id: playlist.id,
      name: playlist.title,
      description: playlist.description,
      image: playlist.image,
      songs: [playlist.featuredSong],
    })),
    ...popularRadio.map((radio) => ({
      id: radio.id,
      name: radio.title,
      description: radio.description,
      image: radio.image,
      songs: [radio.featuredSong],
    })),
  ];

  return playlists;
}

export function getPlaylistById(id: string): Playlist | undefined {
  return getAllPlaylists().find((playlist) => playlist.id === id);
}
